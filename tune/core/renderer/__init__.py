def render_samtools_sort(
    params: dict, bindings: dict, output_dir: str, renderer_version: int = 1
) -> RenderedCommand:
    input_file = bindings.get("input_sam_or_bam") or bindings.get("aligned_bam")
    if not input_file:
        raise RendererError("Required slot 'input_sam_or_bam' is not bound")

    threads = _threads(params)
    mem = params.get("memory_per_thread", "768M")

    sample = Path(input_file).name.rsplit(".", 1)[0]
    out_bam = os.path.join(output_dir, f"{sample}_sorted.bam")

    cmd = (
        f"samtools sort -@ {threads} -m {mem}"
        f" -o {out_bam} {input_file}"
    )

    return RenderedCommand(
        command_text=cmd,
        expected_outputs=[out_bam],
        renderer_version=renderer_version,
    )


