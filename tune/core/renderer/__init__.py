"""Command renderers for bioinformatics tools.

Each renderer is a pure function:
  render_*(params, bindings, output_dir) -> RenderedCommand

This gives deterministic, testable command generation without LLM involvement.

Phase 3 additions:
- RenderedCommand carries template_type, command_fingerprint, safety_flags
- render_step() fills these fields automatically from the step type registry
- After RENDERER_REGISTRY is built, each renderer is linked to its
  StepTypeDefinition.renderer so the registry is the single source of truth
"""
from __future__ import annotations

import hashlib
import importlib
import os
import shlex
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any


@dataclass
class RenderedCommand:
    command_text: str
    # Phase-3 metadata fields (filled by render_step() automatically)
    template_type: str = ""          # step_type, e.g. "qc.fastqc"
    command_fingerprint: str = ""    # short hash of template_type + param keys
    safety_flags: list[str] = field(default_factory=list)
    # Existing fields
    env_vars: dict[str, str] = field(default_factory=dict)
    expected_outputs: list[str] = field(default_factory=list)
    renderer_version: int = 1
    # Phase-5: True when the command was generated by LLM fallback instead of the deterministic renderer
    llm_fallback_used: bool = False


class RendererError(ValueError):
    """Raised when a renderer cannot produce a valid command (missing slot, invalid param)."""


def _require(bindings: dict, slot: str) -> str:
    """Return bindings[slot], raising RendererError if missing."""
    val = bindings.get(slot)
    if not val:
        raise RendererError(f"Required slot '{slot}' is not bound")
    return val


def _threads(params: dict, default: int = 4) -> int:
    return int(params.get("threads", default))


def _compute_fingerprint(template_type: str, params: dict) -> str:
    """Stable short hash of template_type + sorted param keys.

    Used as a cache key for authorization (command_type) and repair memory
    matching.  Intentionally excludes param *values* and file paths so the
    same step run against different samples produces the same fingerprint.
    """
    key = template_type + "::" + ",".join(sorted(params.keys()))
    return hashlib.sha256(key.encode()).hexdigest()[:12]


def _stage_gzip_file(
    source_path: str,
    output_dir: str,
    staged_name: str,
) -> tuple[str, str] | None:
    """Return (prepare_cmd, staged_path) when a gzipped file must be unpacked."""
    if not str(source_path).endswith(".gz"):
        return None
    staged_path = os.path.join(output_dir, staged_name)
    prepare_cmd = f"gzip -dc {shlex.quote(source_path)} > {shlex.quote(staged_path)}"
    return prepare_cmd, staged_path


def _stage_gzip_annotation(annotation_path: str, output_dir: str) -> tuple[str, str] | None:
    """Return (prepare_cmd, staged_path) when a gzipped annotation must be unpacked."""
    return _stage_gzip_file(annotation_path, output_dir, "annotation.gtf")


def _stringify_template_value(value: Any) -> str:
    if value is None:
        return ""
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, (list, tuple, set)):
        return " ".join(_stringify_template_value(item) for item in value)
    return str(value)


def _shell_quote_template_value(value: Any) -> str:
    if isinstance(value, (list, tuple, set)):
        return " ".join(shlex.quote(_stringify_template_value(item)) for item in value)
    return shlex.quote(_stringify_template_value(value))


def _format_template(template: str, context: dict[str, str], label: str) -> str:
    try:
        return template.format_map(context)
    except KeyError as exc:
        raise RendererError(f"Template field '{exc.args[0]}' is not available in {label}") from exc


def _build_declarative_context(
    step_type: str,
    params: dict[str, Any],
    bindings: dict[str, Any],
    output_dir: str,
) -> tuple[dict[str, str], dict[str, str]]:
    raw_context = {
        "step_type": step_type,
        "output_dir": output_dir,
    }
    quoted_context = {
        "step_type": step_type,
        "output_dir": shlex.quote(output_dir),
    }

    for source_name, source_values in (
        ("param", params or {}),
        ("binding", bindings or {}),
    ):
        for key, value in source_values.items():
            key_name = str(key)
            raw_value = _stringify_template_value(value)
            quoted_value = _shell_quote_template_value(value)
            raw_context[key_name] = raw_value
            quoted_context[key_name] = quoted_value
            raw_context[f"{source_name}_{key_name}"] = raw_value
            quoted_context[f"{source_name}_{key_name}"] = quoted_value

    return raw_context, quoted_context


def render_declarative_step(
    step_type: str,
    renderer_spec: dict[str, Any],
    params: dict,
    bindings: dict,
    output_dir: str,
) -> RenderedCommand:
    command_template = str(renderer_spec.get("command") or "").strip()
    if not command_template:
        raise RendererError(f"Declarative renderer for '{step_type}' is missing template.command")

    raw_context, quoted_context = _build_declarative_context(step_type, params, bindings, output_dir)

    output_bindings_raw: dict[str, str] = {}
    for slot_name, output_template in (renderer_spec.get("output_bindings") or {}).items():
        slot_key = str(slot_name)
        output_bindings_raw[slot_key] = _format_template(
            str(output_template),
            raw_context,
            f"{step_type} output binding '{slot_key}'",
        )

    for slot_key, raw_value in output_bindings_raw.items():
        quoted_value = shlex.quote(raw_value)
        raw_context[slot_key] = raw_value
        quoted_context[slot_key] = quoted_value
        raw_context[f"output_{slot_key}"] = raw_value
        quoted_context[f"output_{slot_key}"] = quoted_value

    command = _format_template(command_template, quoted_context, f"{step_type} command")
    env_vars = {
        str(key): _format_template(str(value), raw_context, f"{step_type} env var '{key}'")
        for key, value in (renderer_spec.get("env_vars") or {}).items()
    }

    expected_outputs = list(output_bindings_raw.values()) or [output_dir]
    return RenderedCommand(
        command_text=command,
        env_vars=env_vars,
        expected_outputs=expected_outputs,
        renderer_version=int(renderer_spec.get("renderer_version", 1)),
    )


# ---------------------------------------------------------------------------
# qc.fastqc
# ---------------------------------------------------------------------------

def render_fastqc(
    params: dict, bindings: dict, output_dir: str, renderer_version: int = 1
) -> RenderedCommand:
    reads = bindings.get("reads") or bindings.get("read1")
    if not reads:
        raise RendererError("Required slot 'reads' (or 'read1') is not bound")

    threads = _threads(params)

    # reads may be a single path or comma-separated list
    if isinstance(reads, list):
        reads_args = " ".join(reads)
    elif "," in str(reads):
        reads_args = reads
    else:
        reads_args = str(reads)

    cmd = f"fastqc {reads_args} -o {output_dir} -t {threads}"
    return RenderedCommand(
        command_text=cmd,
        expected_outputs=[output_dir],
        renderer_version=renderer_version,
    )


