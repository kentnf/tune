# Phase 3: link each renderer to its StepTypeDefinition so the registry is
# the single source of truth.  This runs at import time; import order is
# safe because registry/steps.py has no dependency on renderer.
from tune.core.registry import get_step_type as _get_step_type  # noqa: E402
for _step_type, _fn in RENDERER_REGISTRY.items():
    _defn = _get_step_type(_step_type)
    if _defn is not None:
        _defn.renderer = _fn


def render_step(
    step_type: str,
    params: dict,
    bindings: dict,
    output_dir: str,
) -> RenderedCommand:
    """Dispatch to the appropriate renderer. Raises RendererError on unknown step_type.

    Phase-3 behaviour: prefers defn.renderer (set at import time) over the
    static RENDERER_REGISTRY dict, then fills template_type, command_fingerprint,
    and safety_flags on the returned RenderedCommand automatically.
    """
    from tune.core.registry import ensure_registry_loaded, get_step_type

    ensure_registry_loaded()
    ensure_renderers_loaded()
    defn = get_step_type(step_type)

    renderer_fn = (defn.renderer if defn and defn.renderer else None) or RENDERER_REGISTRY.get(step_type)
    if renderer_fn is None and defn and defn.renderer_spec:
        result = render_declarative_step(step_type, defn.renderer_spec, params, bindings, output_dir)
    elif renderer_fn is None:
        raise RendererError(
            f"No renderer registered for step_type '{step_type}'. "
            f"Available: {sorted(RENDERER_REGISTRY)}"
        )
    else:
        result = renderer_fn(params, bindings, output_dir)

    # Fill Phase-3 metadata fields if not already set by the renderer
    if not result.template_type:
        result.template_type = step_type
    if not result.command_fingerprint:
        result.command_fingerprint = _compute_fingerprint(step_type, params)
    if not result.safety_flags and defn and defn.safety_policy.safety_flags:
        result.safety_flags = list(defn.safety_policy.safety_flags)

    return result
