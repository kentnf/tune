def render_star_genome_generate(
    params: dict, bindings: dict, output_dir: str, renderer_version: int = 1
) -> RenderedCommand:
    fasta = _require(bindings, "reference_fasta")
    threads = _threads(params)
    genome_sa_nbases = int(params.get("genome_sa_index_nbases") or 14)
    genome_dir = os.path.join(output_dir, "star_genome")
    os.makedirs(genome_dir, exist_ok=True)

    parts = [
        "STAR",
        "--runMode genomeGenerate",
        f"--genomeDir {genome_dir}",
        f"--genomeFastaFiles {fasta}",
        f"--runThreadN {threads}",
        f"--genomeSAindexNbases {genome_sa_nbases}",
    ]

    gtf = bindings.get("annotation_gtf")
    prepare_cmds: list[str] = []
    staged_annotation = _stage_gzip_annotation(gtf, output_dir) if gtf else None
    if staged_annotation:
        prepare_cmd, staged_path = staged_annotation
        prepare_cmds.append(prepare_cmd)
        gtf = staged_path
    if gtf:
        parts.append(f"--sjdbGTFfile {gtf}")

    star_cmd = " ".join(parts)
    cmd = " && ".join(prepare_cmds + [star_cmd]) if prepare_cmds else star_cmd
    return RenderedCommand(
        command_text=cmd,
        expected_outputs=[os.path.join(genome_dir, "SA")],
        renderer_version=renderer_version,
    )


