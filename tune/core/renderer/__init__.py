def ensure_renderers_loaded() -> None:
    """Re-link built-in renderers after unusual reload/import ordering."""
    from tune.core.registry import ensure_registry_loaded, get_step_type as _get_step_type

    ensure_registry_loaded()

    builtins = {
        "qc.fastqc": render_fastqc,
        "qc.multiqc": render_multiqc,
        "trim.fastp": render_fastp,
        "align.bwa": render_bwa,
        "align.hisat2": render_hisat2,
        "align.star": render_star,
        "quant.featurecounts": render_featurecounts,
        "stats.deseq2": render_deseq2,
        "util.bwa_index": render_bwa_index,
        "util.hisat2_build": render_hisat2_build,
        "util.star_genome_generate": render_star_genome_generate,
        "util.samtools_sort": render_samtools_sort,
        "util.samtools_index": render_samtools_index,
    }
    missing = [step_type for step_type in builtins if step_type not in RENDERER_REGISTRY]
    if missing:
        importlib.import_module("tune.core.registry.steps")
        RENDERER_REGISTRY.update(builtins)

    for step_type, fn in builtins.items():
        defn = _get_step_type(step_type)
        if defn is not None and defn.renderer is None:
            defn.renderer = fn


